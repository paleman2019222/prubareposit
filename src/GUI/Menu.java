    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        Connection cn = connect.conectar();
        LoginView lg = new LoginView();
        lg.setVisible(false);
       try {
         
           
           PreparedStatement  pps = cn.prepareStatement("INSERT INTO vendedor (idvendedor, nombreVendedor, apellidoVendedor, direccionVendedor ) values (?,?,?,?)");
            int id= Integer.parseInt(txtId.getText());
           pps.setInt(1,id);
         pps.setString(2,txtNombre.getText());
         pps.setString(3,txtApellidos.getText());
         pps.setString(4,txtDireccion.getText());
        
         pps.executeUpdate();
         JOptionPane.showMessageDialog(null, "Datos guardados");// TODO add your handling code here:
    }                                       
    catch (SQLException ex) {
         JOptionPane.showMessageDialog(null, "Error al guardar los datos ");
         
          
       }
    }//GEN-LAST:event_btnGuardarActionPerformed

